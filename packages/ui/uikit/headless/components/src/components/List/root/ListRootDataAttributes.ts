export enum ListRootDataAttributes {
    /**
     * The layout orientation of the list.
     */
    orientation = 'data-orientation',
    /**
     * Whether the list is nested within another list.
     */
    nested = 'data-nested',
    /**
     * The number of the nested list.
     */
    nestedListNumber = 'data-nested-list-number',
    /**
     * The number of the nested list item.
     */
    nestedListItemNumber = 'data-nested-list-item-number',
    /**
     * The type of the list.
     */
    type = 'data-type'
}
